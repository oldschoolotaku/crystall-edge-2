using Content.Shared._CE.Workbench;
using Content.Shared._CE.Workbench.Prototypes;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Prototypes;

namespace Content.Client._CE.Workbench;

[GenerateTypedNameReferences]
public sealed partial class CEWorkbenchRecipeControl : Control
{
    [Dependency] private readonly IEntityManager _entity = default!;
    [Dependency] private readonly IPrototypeManager _prototype = default!;

    public event Action<CEWorkbenchUiRecipesEntry, CEWorkbenchRecipePrototype>? OnSelect;

    private readonly SpriteSystem _sprite;

    private readonly CEWorkbenchRecipePrototype _recipePrototype;
    private readonly bool _craftable;

    public CEWorkbenchRecipeControl(CEWorkbenchUiRecipesEntry entry)
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _sprite = _entity.System<SpriteSystem>();

        _recipePrototype = _prototype.Index(entry.ProtoId);
        _craftable = entry.Craftable;

        Button.OnPressed += _ => OnSelect?.Invoke(entry, _recipePrototype);

        UpdateColor();
        UpdateView();
    }

    private void UpdateColor()
    {
        if (_craftable)
            return;

        Button.ModulateSelfOverride = Color.FromHex("#302622");
    }

    private void UpdateView()
    {
        View.SetPrototype(_recipePrototype.Result);
    }
}
